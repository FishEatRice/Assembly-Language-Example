.MODEL SMALL
.STACK 100
.DATA
	NUM DB 1
	TOTAL DB 0
	TEN DB 10
	SPACE DB " $"
	STR1 DB "Total is $"
	NL DB 0AH,0DH,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV CX,6

	L1:	
		MOV AH,02H
		MOV DL,NUM
		ADD TOTAL,DL
		ADD DL,30H
		INT 21H
		INC NUM
		
		MOV AH,09H
		LEA DX,SPACE
		INT 21H
	LOOP L1
	
	MOV AH,0H
	MOV AL,TOTAL
	DIV TEN
	MOV BX,AX
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV AH,09H
	LEA DX,STR1
	INT 21H
	
	MOV AH,02H
	MOV DL,BL
	ADD DL,30H
	INT 21H
	
	MOV AH,02H
	MOV DL,BH
	ADD DL,30H
	INT 21H
	
	MOV AX,4c00H
	INT 21H
MAIN ENDP
END MAIN