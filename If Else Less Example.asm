.MODEL SMALL
.STACK 100
.DATA
	DISPLAY1 DB "Enter num1: $"
	DISPLAY2 DB "Enter num2: $"
	INPUT1 DB ?
	INPUT2 DB ?
	NL DB 0AH,0DH,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,DISPLAY1
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV INPUT1, AL
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV AH,09H
	LEA DX,DISPLAY2
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV INPUT2, AL
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV AH,INPUT2
	CMP INPUT1,AH
	JLE BIG2
	
	; IF INPUT1 is Bigger
	MOV AH,02H
	MOV DL,INPUT1
	ADD DL,30H
	INT 21H
	
	JMP FIN
	
	BIG2:
		MOV AH,02H
		MOV DL,INPUT2
		ADD DL,30H
		INT 21H
	
	FIN:
	
	MOV AX,4c00H
	INT 21H
MAIN ENDP
END MAIN