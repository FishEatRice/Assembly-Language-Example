.MODEL SMALL
.STACK 100
.DATA
	MSG DB "Enter a word: $"
	NL DB 0AH,0DH,"$"
	STR LABEL BYTE
	MAXN DB 20
	ACTN DB ?
	INPURSTR DB 20 DUP("$")
	RESULT DB "The third character is $"
	MSG2 DB "Enter a digit: $"
	input DB ?

.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,MSG
	INT 21H
	
	;INPUT STRING 
	MOV AH,0AH
	LEA DX,STR
	INT 21H
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV AH,09H
	LEA DX,MSG2
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV input,AL
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV SI,0
	MOV CL,ACTN
	MOV CH,0
	L1:
		MOV AH,02H
		MOV DL,INPURSTR[SI]
		SUB DL,30H
		Add DL,input
		ADD DL,30H
		INT 21H
		
		INC SI
	LOOP L1
	
	MOV AX,4c00H
	INT 21H
MAIN ENDP
END MAIN