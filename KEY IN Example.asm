.MODEL SMALL
.STACK 100
.DATA
	NUM1 DB ?
	NUM2 DB 2
	STR1 DB "Enter a digit: $"
	STR2 DB "Remainder is $"
	NL DB 0AH,0DH,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,STR1
	INT 21H

	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV NUM1,AL

	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV AH,09H
	LEA DX,STR2
	INT 21H
	
	MOV AX,0
	MOV AL,NUM1
	DIV NUM2
	MOV BX,AX
	
	MOV AH,02H
	MOV DL,BH
	ADD DL,30H
	INT 21H

	MOV AX,4c00H
	INT 21H
MAIN ENDP
END MAIN