.MODEL SMALL
.STACK 100
.DATA
	STRPASS DB "PASS$"
	STRFAIL DB "FAIL$"
	DIGIT DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV DIGIT, AL
	
	CMP DIGIT,5
	JGE LPASS ; JAE LPASS (UNSIGNED)
	
	;FAILS
	MOV AH,09H
	LEA DX,STRFAIL
	INT 21H
	
	JMP FINISH
	
	;PASS
	LPASS:
		MOV AH,09H
		LEA DX,STRPASS
		INT 21H
		
	FINISH:
	
	MOV AX,4c00H
	INT 21H
MAIN ENDP
END MAIN