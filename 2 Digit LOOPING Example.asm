.MODEL SMALL
.STACK 100
.DATA
	NUM DB 17
	SPACE DB " $"
	NL DB 0AH,0DH,"$"
	TEN DB 10
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV CX,5

	L1:	
		MOV AH,0
		MOV AL,NUM
		DIV TEN
		MOV BX,AX
		
		MOV AH,02H
		MOV DL,BL
		ADD DL,30H
		INT 21H
		
		MOV AH,02H
		MOV DL,BH
		ADD DL,30H
		INT 21H
		
		MOV AH,09H
		LEA DX,SPACE
		INT 21H
		
		ADD NUM,1
	LOOP L1
	
	MOV AX,4c00H
	INT 21H
MAIN ENDP
END MAIN