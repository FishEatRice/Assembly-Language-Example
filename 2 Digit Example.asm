.MODEL SMALL
.STACK 100
.DATA
	NUM DB 26
	TEN DB 10
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX

	;26(AX)/10
	MOV AH,0H
	MOV AL,NUM
	DIV TEN
	MOV BX, AX
	
	MOV AH,02H
	MOV DL,BL
	ADD DL,30H
	INT 21H
	
	MOV AH,02H
	MOV DL,BH
	ADD DL,30H
	INT 21H

	MOV AX,4c00H
	INT 21H
MAIN ENDP
END MAIN