.MODEL SMALL
.STACK 100
.DATA
	NUM DB 2,5,6,8,1,7
	STR DB "Enter a digit: $"
	NL DB 0AH,0DH,"$"
	INPUT DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,STR
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV INPUT,AL
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV SI,0
	MOV CX,0
	MOV CL,INPUT
	SHOW:
		MOV AH,02H
		MOV DL,NUM[SI]
		ADD DL,30H
		INT 21H
		INC SI
	LOOP SHOW
	
	MOV AX,4c00H
	INT 21H
MAIN ENDP
END MAIN