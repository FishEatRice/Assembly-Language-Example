.MODEL SMALL
.STACK 100
.DATA
	N1 DB 4
	N2 DB 9
	TEN DB 10
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX

	MOV AH,0H
	MOV AL,N1
	MUL N2
	
	DIV TEN
	MOV BX,AX
	
	MOV AH,02H
	MOV DL,BL
	ADD DL,30H
	INT 21H
	
	MOV AH,02H
	MOV DL,BH
	ADD DL,30H
	INT 21H

	MOV AX,4c00H
	INT 21H
MAIN ENDP
END MAIN